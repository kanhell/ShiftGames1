/// <summary>
/// 아이템 타입 정의
/// </summary>
public enum ItemType
{
    Weapon,      // 무기 (검, 활, 지팡이 등)
    Armor,       // 갑옷/방어구 (상의)
    Helmet,      // 투구/모자
    Shoes,       // 신발/부츠
    Bag,         // 가방 (인벤토리 확장용)
    Quiver,      // 화살통 (궁수 전용)
    Consumable,  // 소비 아이템 (포션, 음식 등)
    Quest,       // 퀘스트 아이템
    Etc          // 기타 아이템
}
